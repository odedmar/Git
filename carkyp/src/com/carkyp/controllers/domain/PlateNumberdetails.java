package com.carkyp.controllers.domain;

public class PlateNumberdetails {
	
	private String spid;
	private String plateNo;
	private String registrationDate;
	private String vinNumber;
	
	
	
	
	public final String getRegistrationDate() {
		return registrationDate;
	}
	public final void setRegistrationDate(String registrationDate) {
		this.registrationDate = registrationDate;
	}
	public final String getVinNumber() {
		return vinNumber;
	}
	public final void setVinNumber(String vinNumber) {
		this.vinNumber = vinNumber;
	}
	public final String getSpid() {
		return spid;
	}
	public final void setSpid(String spid) {
		this.spid = spid;
	}
	public final String getPlateNo() {
		return plateNo;
	}
	public final void setPlateNo(String plateNo) {
		this.plateNo = plateNo;
	}
	
	

}
